function initCountdown() {
  const target = new Date("2026-11-01T00:00:00+07:00").getTime();

  function update() {
    const distance = Math.max(0, target - Date.now());
    const days = Math.floor(distance / 86400000);
    const hours = Math.floor((distance % 86400000) / 3600000);
    const minutes = Math.floor((distance % 3600000) / 60000);
    const seconds = Math.floor((distance % 60000) / 1000);

    $("#days").textContent = String(days).padStart(3, "0");
    $("#hours").textContent = String(hours).padStart(2, "0");
    $("#minutes").textContent = String(minutes).padStart(2, "0");
    $("#seconds").textContent = String(seconds).padStart(2, "0");
  }

  update();
  window.setInterval(update, 1000);
}

function initParticles() {
  const canvas = $("#particleCanvas");
  const context = canvas.getContext("2d");
  const prefersReducedMotion = matchMedia("(prefers-reduced-motion: reduce)").matches;
  let particles = [];
  let animationFrame;
  let isVisible = true;

  function resize() {
    const ratio = Math.min(window.devicePixelRatio || 1, 2);
    canvas.width = canvas.clientWidth * ratio;
    canvas.height = canvas.clientHeight * ratio;
    context.setTransform(ratio, 0, 0, ratio, 0, 0);
    const count = Math.min(54, Math.floor(canvas.clientWidth / 22));
    particles = Array.from({ length: count }, () => ({
      x: Math.random() * canvas.clientWidth,
      y: Math.random() * canvas.clientHeight,
      radius: Math.random() * 1.6 + 0.35,
      speed: Math.random() * 0.18 + 0.05,
      drift: (Math.random() - 0.5) * 0.12,
      alpha: Math.random() * 0.45 + 0.16,
      gold: Math.random() > 0.72,
      leaf: Math.random() > 0.86,
      angle: Math.random() * Math.PI * 2,
      wobble: Math.random() * Math.PI * 2,
    }));
  }

  function draw() {
    animationFrame = null;
    if (!isVisible) return;
    context.clearRect(0, 0, canvas.clientWidth, canvas.clientHeight);
    particles.forEach((particle) => {
      context.fillStyle = particle.gold
        ? `rgba(231, 190, 106, ${particle.alpha})`
        : `rgba(120, 240, 189, ${particle.alpha})`;
      context.shadowBlur = 10;
      context.shadowColor = particle.gold ? "#e7be6a" : "#78f0bd";
      context.beginPath();
      if (particle.leaf) {
        context.save();
        context.translate(particle.x, particle.y);
        context.rotate(particle.angle);
        context.ellipse(0, 0, particle.radius * 2.8, particle.radius * 0.95, 0.35, 0, Math.PI * 2);
        context.fill();
        context.restore();
        particle.angle += 0.006;
      } else {
        context.arc(particle.x, particle.y, particle.radius, 0, Math.PI * 2);
        context.fill();
      }
      particle.y -= particle.speed;
      particle.wobble += 0.008;
      particle.x += particle.drift + Math.sin(particle.wobble) * 0.045;
      if (particle.y < -10) {
        particle.y = canvas.clientHeight + 10;
        particle.x = Math.random() * canvas.clientWidth;
      }
    });
    if (!prefersReducedMotion) animationFrame = requestAnimationFrame(draw);
  }

  resize();
  draw();
  if ("IntersectionObserver" in window) {
    const visibilityObserver = new IntersectionObserver(
      ([entry]) => {
        isVisible = entry.isIntersecting;
        if (!isVisible && animationFrame) {
          cancelAnimationFrame(animationFrame);
          animationFrame = null;
        } else if (isVisible && !animationFrame && !prefersReducedMotion) {
          draw();
        }
      },
      { threshold: 0.01 },
    );
    visibilityObserver.observe(canvas);
  }

  window.addEventListener("resize", () => {
    cancelAnimationFrame(animationFrame);
    animationFrame = null;
    resize();
    draw();
  });
}

function initAmbientSections() {
  const sections = $$(
    ".intro, .map-section, .garden-section, .nations-section, .events-section, .gallery-section, .news-section, .visit-section",
  );

  if (!("IntersectionObserver" in window)) {
    sections.forEach((section) => section.classList.add("ambient-active"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        entry.target.classList.toggle("ambient-active", entry.isIntersecting);
      });
    },
    { rootMargin: "18% 0px 18% 0px", threshold: 0.01 },
  );

  sections.forEach((section) => observer.observe(section));
}

function initCinematicMotion() {
  const hero = $(".hero");
  const heroImages = $$(".hero-image");
  const prefersReducedMotion = matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (!hero || !heroImages.length || prefersReducedMotion) return;

  let pointerX = 0;
  let pointerY = 0;
  let scrollOffset = 0;
  let frameRequested = false;

  function renderMotion() {
    heroImages.forEach((heroImage, index) => {
      const depth = 1 - index * 0.13;
      heroImage.style.setProperty("--hero-x", `${pointerX * 13 * depth}px`);
      heroImage.style.setProperty("--hero-y", `${scrollOffset + pointerY * 7 * depth}px`);
    });
    frameRequested = false;
  }

  function requestMotionFrame() {
    if (!frameRequested) {
      frameRequested = true;
      requestAnimationFrame(renderMotion);
    }
  }

  if (matchMedia("(pointer: fine)").matches) {
    hero.addEventListener(
      "pointermove",
      (event) => {
        const rect = hero.getBoundingClientRect();
        pointerX = (event.clientX - rect.left) / rect.width - 0.5;
        pointerY = (event.clientY - rect.top) / rect.height - 0.5;
        requestMotionFrame();
      },
      { passive: true },
    );

    hero.addEventListener("pointerleave", () => {
      pointerX = 0;
      pointerY = 0;
      requestMotionFrame();
    });
  }

  window.addEventListener(
    "scroll",
    () => {
      scrollOffset = Math.min(window.scrollY * 0.11, 95);
      requestMotionFrame();
    },
    { passive: true },
  );

  document.addEventListener("visibilitychange", () => {
    document.body.classList.toggle("motion-paused", document.hidden);
  });
}

function initReveal() {
  $$(".hero .reveal").forEach((element) => element.classList.add("is-visible"));

  const revealVisibleElements = () => {
    $$(".reveal:not(.is-visible)").forEach((element) => {
      const rect = element.getBoundingClientRect();
      if (rect.top < window.innerHeight * 1.05 && rect.bottom > 0) {
        element.classList.add("is-visible");
      }
    });
  };

  if (!("IntersectionObserver" in window)) {
    $$(".reveal").forEach((element) => element.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12 },
  );

  $$(".reveal:not(.is-visible)").forEach((element) => observer.observe(element));
  window.addEventListener("load", () => window.setTimeout(revealVisibleElements, 180));
  window.addEventListener("hashchange", () => window.setTimeout(revealVisibleElements, 180));
}

function initNavigation() {
  const header = $("#siteHeader");
  const menuToggle = $("#menuToggle");
  const navLinks = $("#navLinks");

  window.addEventListener(
    "scroll",
    () => header.classList.toggle("is-scrolled", window.scrollY > 35),
    { passive: true },
  );

  menuToggle.addEventListener("click", () => {
    const open = !navLinks.classList.contains("is-open");
    navLinks.classList.toggle("is-open", open);
    menuToggle.classList.toggle("is-open", open);
    menuToggle.setAttribute("aria-expanded", String(open));
  });

  $$(".nav-links a").forEach((link) => {
    link.addEventListener("click", () => {
      navLinks.classList.remove("is-open");
      menuToggle.classList.remove("is-open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });

  const sections = $$("main section[id]");
  const navObserver = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!visible) return;
      $$(".nav-links a").forEach((link) => {
        link.classList.toggle("is-active", link.getAttribute("href") === `#${visible.target.id}`);
      });
    },
    { rootMargin: "-25% 0px -60% 0px", threshold: [0, 0.25, 0.5] },
  );
  sections.forEach((section) => navObserver.observe(section));
}

