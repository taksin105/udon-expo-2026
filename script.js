const $ = (selector, scope = document) => scope.querySelector(selector);
const $$ = (selector, scope = document) => [...scope.querySelectorAll(selector)];

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "th";
  const dict = translations[currentLanguage];

  document.documentElement.lang = currentLanguage === "zh" ? "zh-CN" : currentLanguage;
  document.title =
    currentLanguage === "th"
      ? "Udon Thani Expo 2026 — ความหลากหลายแห่งสรรพชีวิต"
      : currentLanguage === "zh"
        ? "2026 乌隆他尼世界园艺博览会 — 生命多样性"
        : "Udon Thani Expo 2026 — Diversity of Life";

  $$("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dict[key] !== undefined) element.textContent = dict[key];
  });

  $$("[data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dict[key] !== undefined) element.innerHTML = dict[key];
  });

  $$("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (dict[key] !== undefined) element.placeholder = dict[key];
  });

  $$(".lang-btn").forEach((button) => {
    const active = button.dataset.lang === currentLanguage;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  updateZone(currentZone);
  updateEvent(currentEvent);
  renderSchedule(currentPeriod);
  localStorage.setItem("expo-language", currentLanguage);
}

function updateZone(zoneKey) {
  currentZone = zoneKey;
  const zone = zones[currentLanguage][zoneKey];
  $("#zoneNumber").textContent = zone.number;
  $("#zoneTitle").textContent = zone.title;
  $("#zoneDescription").textContent = zone.description;
  $("#zoneTime").textContent = zone.time;
  $("#zoneStops").textContent = zone.stops;

  $$("[data-zone]").forEach((element) => {
    element.classList.toggle("is-active", element.dataset.zone === zoneKey);
  });
}

function updateEvent(eventKey) {
  currentEvent = eventKey;
  const event = events[currentLanguage][eventKey];
  $("#eventChip").textContent = event.tag;
  $("#eventFeatureTitle").textContent = event.title;
  $("#eventFeatureDescription").textContent = event.body;
  $("#eventDate").textContent = event.date;
  $("#eventTime").textContent = event.time;
  $$(".timeline-item").forEach((element) => {
    element.classList.toggle("is-active", element.dataset.event === eventKey);
  });
}

